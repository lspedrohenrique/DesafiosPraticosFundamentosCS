namespace MatematicaBasicaEntreDoisValores;

internal static class OperacoesMatematicas
{
    public static double Soma(double numeroA, double numeroB) => numeroA + numeroB;
    public static double Subtrair(double numeroA, double numeroB) => numeroA - numeroB;
    public static double Multiplicar(double numeroA, double numeroB) => numeroA * numeroB;
    public static double Dividir(double numeroA, double numeroB) => numeroA / numeroB;
    public static double Media(double numeroA, double numeroB) => (numeroA + numeroB) / 2;
}
